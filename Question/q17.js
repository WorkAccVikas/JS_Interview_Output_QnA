// TOPIC : switch block

let day = "Tuesday";

switch (day) {
  case "Monday":
    console.log("It's Monday!");
  // break;
  case "Tuesday":
    console.log("It's Tuesday!");
  // break;
  default:
    console.log(`I don't know what ${day} is.`);
}

